HYDE_PARAGRAPH_MAX_CHARS = 1200

QUERY_EXTEND_PROMPT = """Ты помогаешь **векторному поиску** по полному тексту романа «{book_title}».

Вопрос читателя:
{question}

Напиши **ровно два** перефразированных вопроса — тот же смысл, другие слова и порядок мысли, чтобы эмбеддинги отличались от оригинала и находились дополнительные релевантные отрывки.
- Все **имена собственные и узкие термины** из исходного вопроса **сохрани дословно** (не заменяй фамилии на «он», «чиновник» и т.п.).
- Не добавляй новых имён, дат и фактов, которых нет в вопросе.
- Не отвечай на вопрос — только две альтернативные формулировки вопроса.

Формат ответа (строго две строки, каждая с нумерацией):
1) <первый перефразированный вопрос одной строкой>
2) <второй перефразированный вопрос одной строкой>"""


def _normalize_hyde_output(raw: str) -> str:
    text = (raw or "").strip()
    lower = text.lower()
    for prefix in ("абзац:", "текст:", "фрагмент:", "гипотетический абзац:", "вот текст:"):
        if lower.startswith(prefix):
            text = text[len(prefix) :].strip()
            lower = text.lower()
    if len(text) > HYDE_PARAGRAPH_MAX_CHARS:
        cut = text[: HYDE_PARAGRAPH_MAX_CHARS + 1]
        if " " in cut:
            text = cut.rsplit(" ", 1)[0].rstrip(",.;:")
        else:
            text = cut
    return text.strip()


def parse_two_query_paraphrases(raw: str) -> list[str]:
    found: list[str] = []
    for line in (raw or "").splitlines():
        stripped = line.strip()
        if not stripped:
            continue
        match = re.match(r"^\s*\d+\s*[\).:\-–—]\s*(.+)$", stripped)
        if not match:
            continue
        body = match.group(1).strip()
        if len(body) < 8:
            continue
        if body.lower() not in {x.lower() for x in found}:
            found.append(body)
        if len(found) >= 2:
            break
    return found[:2]


def invoke_query_paraphrases(llm: ChatLLM, book_title: str, question: str) -> list[str]:
    prompt = QUERY_EXTEND_PROMPT.format(book_title=book_title, question=question.strip())
    raw = llm.invoke(
        [
            SystemMessage(content="Следуй формату ответа из инструкции пользователя."),
            HumanMessage(content=prompt),
        ]
    )
    return parse_two_query_paraphrases(raw)


def invoke_hyde_paragraph(llm: ChatLLM, hyde_system_prompt: str, question: str) -> str:
    raw = llm.invoke(
        [
            SystemMessage(content=hyde_system_prompt),
            HumanMessage(
                content=(
                    f"Вопрос читателя:\n{question.strip()}\n\n"
                    "Напиши только абзац для поиска по тексту романа. Без вводных фраз и без заголовков."
                )
            ),
        ]
    )
    return _normalize_hyde_output(raw)
